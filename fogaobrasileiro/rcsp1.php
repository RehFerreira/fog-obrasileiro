		<div id="receita">
			<h3>Modo de preparo:</h3>
			<h3>Tutu de feijão:</h3>
				<ol>
					<li>Refogar a cebola e o alho</li>
					<li>Quando a cebola estiver transparente, juntar o feijão e deixar ferver por uns 10 minutos para que tome o gosto dos tempero</li>
					<li>Acrescentar duas xícaras de água e sal a gosto</li>
					<li>Quando estiver encorpado, juntar uma xícara de farinha de mandioca</li>
					<li>Mexer sem parar, até que tome o aspecto de um pirão grosso</li>
				</ol>
			<h3>Bisteca suína:</h3>
				<ol>
					<li>Temperar as bistecas com o suco dos limões, o sal e a pimenta - do - reino a gosto</li>
					<li>Deixar descansar por 1 hora</li>
					<li>Depois, em uma panela pré - aquecida acrescentar o o óleo ou o azeite, as bistecas e fritar com a panela tampada, acrescentando água na medida em que for secando</li>
					<li>Após uns 30 minutos, retirar do fogo e servir como acompanhamento do tutu</li>
				</ol>
			<h3>Linguiça:</h3>
				<ol>
					<li>Cozinhar as lingüiças em água fervente por 20 minutos ou até que estejam completamente cozidas</li>
					<li>Cortar em rodela e fritar em uma panela pré – aquecida</li>
				</ol>
			<h3>Bananas fritas::</h3>
				<ol>
					<li>Cortar as bananas ao meio, passar pelo ovo batido, depois pela farinha de trigo e em seguida pela farinha de rosca</li>
					<li>Fritar em óleo bem quente e por imersão (mergulhando o ingrediente no óleo)</li>
				</ol>
			<h3>Ovos:</h3>
				<ol>
					<li>Em uma frigideira antiaderente e pré - aquecida acrescentar um fio de óleo, em seguida o ovo</li>
					<li>Salpicar o sal, tampar a panela e pingar água até atingir o ponto desejado</li>
					<li>Retirar do cozimento e servir</li>
				</ol>
			<h3>Couve:</h3>
				<ol>
					<li>Retirar os talos das folhas de couve, enrolar as folhas formando um rocambolezinho</li>
					<li>Com uma faca bem afiada e com a ajuda dos dedos para definir a espessura, cortar a couve finamente</li>
					<li>Em uma panela pré - aquecida acrescentar o óleo e o sal</li>
					<li>Refogar a couve rapidamente e servir</li>
				</ol>
			<h3>Torresmo:</h3>
				<ol>
					<li>Cortar a panceta em tiras para torresmo</li>
					<li>Colocar o bicarbonato na água fervente, cozinhar o torresmo na água fervente, por uns 20 minutos ou até que solte bastante espuma</li>
					<li>Depois retirar do cozimento, escorrer e fritar em óleo bem quente e com a panela tampada</li>
					<li>Retirar da fritura e escorrer em papel toalha</li>
				</ol>
